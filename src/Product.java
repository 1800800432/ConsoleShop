public class Product {
    private String productid;
    private String productname;
    private String productdic;
    private String productpri;

    public String getProductid() {
        return productid;
    }

    public void setProductid(String productid) {
        this.productid = productid;
    }

    public String getProductname() {
        return productname;
    }

    public void setProductname(String productname) {
        this.productname = productname;
    }

    public String getProductdic() {
        return productdic;
    }

    public void setProductdic(String productdic) {
        this.productdic = productdic;
    }

    public String getProductpri() {
        return productpri;
    }

    public void setProductpri(String productpri) {
        this.productpri = productpri;
    }
}
